/* 1.(23-24-2考试)C语言-顺序结构05-D
【问题描述】根据公式 d = (2a - 3b) / (c + 5.5)，输入a、b、c，计算并输出d，精确到小数点后四位
【样例输入】
3.6 1.9 4.5
【样例输出】
0.1500 */

#include<stdio.h>

int main() {
    double a, b, c, d;
    
    // 输入三个 double 类型的数据
    scanf("%lf %lf %lf", &a, &b, &c);
    
    // 计算表达式的值
    d = (2 * a - 3 * b) / (c + 5.5);
    
    // 输出结果，精确到小数点后四位
    printf("%.4f\n", d);
    
    return 0;
}
